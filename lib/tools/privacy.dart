Henkilötietojen käsittelyn oikeusperuste

Tietojen keräämisen ja käsittelyn peruste on käyttäjän antama suostumus. Suostumus pyydetään mobiilisovelluksessa sekä hallintapaneelissa ennen ensimmäistä sisäänkirjautumista. Mobiilisovellusta tai hallintapaneelia ei voi käyttää ilman suostumuksen antamista. 

